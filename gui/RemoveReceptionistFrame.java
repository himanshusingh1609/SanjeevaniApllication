    private void loadRecpId() 
    {
        try
        {
            List<String>recpList=ReceptionistsDao.getAllRecpId();
            for(String recpId:recpList)
            {
                jcRecpId.addItem(recpId);
            }
            if(jcRecpId.getItemCount()!=0)
                btnDelete.setEnabled(true);
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
    }
