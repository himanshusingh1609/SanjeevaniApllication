    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String recpId=jcRecpId.getSelectedItem().toString();
        try
        {
            boolean result=ReceptionistsDao.deleteReceptionistById(recpId);
            if(result)
            {
                JOptionPane.showMessageDialog(null, "Receptionist removed successfully!");
                jcRecpId.removeItem(recpId);
                if(jcRecpId.getItemCount()==0)
                {
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "All Receptionists removed!");
                }
            }
        
            else
            {
              JOptionPane.showMessageDialog(null, "Deletion unsuccessful");  
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
                                             

    }//GEN-LAST:event_btnDeleteActionPerformed

